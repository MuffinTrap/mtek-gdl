Font* Font_GetDebugFont()
{
	// NOTE In OpenGL
	// the origo 0,0 is in lower left coordinate
	static Font* debugFont = nullptr;
	if (debugFont == nullptr)
	{
		GLubyte fontImage[height][width][4];

		for(unsigned int y = 0; y < height; y++)
		{
			for(unsigned int x = 0; x < width; x++)
			{
				unsigned int read_y = height-1-y;
				unsigned char index = header_data[read_y * width + x];
				fontImage[y][x][0] = header_data_cmap[index][0];
				fontImage[y][x][1] = header_data_cmap[index][1];
				fontImage[y][x][2] = header_data_cmap[index][2];
				fontImage[y][x][3] = (index == 0) ? 0 : 255; // index 0 is transparent
			}
		}

		GLint alignment;
		glGetIntegerv(GL_UNPACK_ALIGNMENT, &alignment);
		glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
		GLuint texName;
		glGenTextures(1, &texName);
		glBindTexture(GL_TEXTURE_2D, texName);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, fontImage);
		glPixelStorei(GL_UNPACK_ALIGNMENT, alignment);

		Image img;
		Image_SetGLName(&img, texName, width, height, gdl::ColorFormats::RGBA);
		debugFont = Font_Load(&img, 8, 8, ' ');
	}
	return debugFont;
}
